public class Camera {
    int x;
    int y;
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    @Override
    public String toString() {
        return "Camera{" +
                "x=" + x +
                '}';
    }


    public String toStringy() {
        return "Camera{" +
                "y=" + y +
                '}';
    }

}
